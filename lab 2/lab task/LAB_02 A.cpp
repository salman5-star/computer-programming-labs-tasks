#include <iostream>
using namespace std;

int main()
{
    int a = 10;        // integer variable
    float b = 3.14;    // float variable
    double c = 3.14159; // double variable
    char d = 'X';      // character variable
    bool e = true;     // boolean variable

    // print value and size of each variable
    cout << "a=" << a << ", size=" << sizeof(a) << endl;
    cout << "b=" << b << ", size=" << sizeof(b) << endl;
    cout << "c=" << c << ", size=" << sizeof(c) << endl;
    cout << "d=" << d << ", size=" << sizeof(d) << endl;
    cout << "e=" << e << ", size=" << sizeof(e) << endl;

    return 0;  // program ends
}

